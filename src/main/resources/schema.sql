
CREATE Memory TABLE RENT_ROOM(
	ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL PRIMARY KEY ,
	AREA INTEGER NOT NULL,
	DESCRIPTION VARCHAR(1000000),
	LANDLORD_ID INTEGER NOT NULL,
	NAME VARCHAR(255) NOT NULL,
	PRICE DECIMAL(10,2) NOT NULL);
CREATE Memory TABLE TENANT(
	ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL PRIMARY KEY,
	NAME VARCHAR(255) NOT NULL);
CREATE Memory TABLE RESERVATION(
	ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) NOT NULL PRIMARY KEY,
	DATE_FROM DATE NOT NULL,
	DATE_TO DATE NOT NULL,
	LANDLORD_ID INTEGER NOT NULL,
	PRICE DECIMAL(10,2) NOT NULL,
	RENT_ROOM_ID INTEGER,
	TENANT_ID INTEGER,
	CONSTRAINT ROOM_CONSTRAINT FOREIGN KEY(RENT_ROOM_ID) REFERENCES RENT_ROOM(ID),
	CONSTRAINT TENANT_CONSTRAINT FOREIGN KEY(TENANT_ID) REFERENCES TENANT(ID));